                <x-text-input wire:model="name" id="name" class="mt-2" type="text" name="name" required autofocus autocomplete="name" placeholder="Jane Doe" />
                <x-input-error :messages="$errors->get('name')" class="mt-2" />
            </div>

            <div x-data="{
                    strength: '',
                    score(pw) {
                        let s = 0;
                        if (pw.length >= 8)  s++;
                        if (pw.length >= 12) s++;
                        if (/[A-Z]/.test(pw)) s++;
                        if (/[0-9]/.test(pw)) s++;
                        if (/[^A-Za-z0-9]/.test(pw)) s++;
                        return s;
                    },
                    refresh(v) { this.strength = v; $wire.set('passwordStrength', v); }
                }">
                <x-input-label for="password" :value="__('Password')" />
                <x-text-input wire:model.live="password" id="password" class="mt-2"
                                type="password"
                                name="password"
                                required autocomplete="new-password"
                                placeholder="At least 8 characters"
                                x-on:input="refresh(
                                    ['empty','weak','fair','good','strong'][Math.min(4, score($event.target.value))]
                                )" />
                <p class="mt-1 text-xs text-brand-slate">Use at least 8 characters. Mix letters, numbers, and a symbol for a stronger password.</p>

                <div class="mt-2 flex items-center gap-2" x-show="strength && strength !== 'empty'">
                    <div class="h-1.5 flex-1 overflow-hidden rounded-full bg-brand-mist">
                        <div class="h-full transition-all"
                             :class="{
                                 'w-1/4 bg-rose-500':       strength === 'weak',
                                 'w-2/4 bg-amber-500':      strength === 'fair',
                                 'w-3/4 bg-blue-500':       strength === 'good',
                                 'w-full bg-brand-green-2': strength === 'strong',
                             }"></div>
                    </div>
                    <span class="text-xs font-medium"
                          :class="{
                              'text-rose-600':       strength === 'weak',
                              'text-amber-600':      strength === 'fair',
                              'text-blue-600':       strength === 'good',
                              'text-brand-green-2':  strength === 'strong',
                          }"
                          x-text="{
                              'weak':   'Weak',
                              'fair':   'Fair',
                              'good':   'Good',
                              'strong': 'Strong',
                          }[strength] || ''"></span>
                </div>
                <x-input-error :messages="$errors->get('password')" class="mt-2" />
            </div>

            <div>
                <x-input-label for="password_confirmation" :value="__('Confirm password')" />
                <x-text-input wire:model="password_confirmation" id="password_confirmation" class="mt-2"
                                type="password"
                                name="password_confirmation" required autocomplete="new-password" placeholder="Re-enter your password" />
                <x-input-error :messages="$errors->get('password_confirmation')" class="mt-2" />
            </div>

            <x-primary-button class="w-full justify-center" wire:loading.attr="disabled" wire:target="register">
                <span wire:loading.remove wire:target="register">{{ __('Create my account') }}</span>
                <span wire:loading wire:target="register" class="inline-flex items-center gap-2">
                    <svg class="h-4 w-4 animate-spin" viewBox="0 0 24 24" fill="none">
                        <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
                        <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8v4a4 4 0 00-4 4H4z"></path>
                    </svg>
                    {{ __('Creating account…') }}
                </span>
            </x-primary-button>
        </form>

        <p class="mt-6 text-center text-sm text-brand-slate">
            {{ __('Already have an account?') }}
            <a href="{{ route('login') }}" wire:navigate
               class="font-semibold text-brand-blue hover:text-brand-blue-3 underline-offset-2 hover:underline">
                {{ __('Sign in') }}
            </a>
        </p>
    @endif
</div>
