<?php

use App\Models\CustomerInvite;
use App\Models\User;
use App\Services\MondayCustomerDirectory;
use Illuminate\Auth\Events\Registered;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Hash;
use Illuminate\Validation\Rules;
use Livewire\Attributes\Layout;
use Livewire\Volt\Component;

/**
 * Customer registration.
 *
 * Two entry points, one component:
 *
 *   1. /register (no token) — show a quiet "you need an invite link"
 *      page. We do NOT allow free sign-ups because every portal
 *      customer must already be on file in the monday.com
 *      Customer Details board — that's the auth.
 *
 *   2. /register/{token} — the actual sign-up form. The token was
 *      generated by `php artisan portal:invite-customer` from a
 *      monday.com record, and the customer identity (email,
 *      account, branch, region, address) is snapshotted onto the
 *      invite. The form pre-fills those fields and locks them,
 *      so the customer can only type their own name + password.
 */
new #[Layout('layouts.guest')] class extends Component
{
    /**
     * The token from the URL, if any. Empty for the bare /register
     * page. Populated by Livewire from the route param.
     */
    public string $token = '';

    /**
     * Snapshot fields — these come from the invite (which got them
     * from monday.com) and the customer cannot change them.
     */
    public string $email       = '';
    public string $accountName = '';
    public string $branch      = '';
    public string $region      = '';
    public string $address     = '';

    /**
     * Customer-supplied fields.
     */
    public string $name                = '';
    public string $password            = '';
    public string $password_confirmation = '';

    /**
     * Live-updated password strength: weak | fair | good | strong.
     */
    public string $passwordStrength = '';

    /**
     * Set to true when the URL doesn't carry a valid token. The
     * blade uses this to swap the form for the "request an invite"
     * message.
     */
    public bool $needsInvite = false;

    /**
     * Set to true when the token is well-formed but has been used
     * or has expired. The blade shows an explanation.
     */
    public bool $inviteInvalid = false;

    public ?string $inviteInvalidReason = null;

    /**
     * Mount: load the invite (if any) and seed the snapshot fields.
     */
    public function mount(MondayCustomerDirectory $directory): void
    {
        $token = trim($this->token);
        if ($token === '') {
            $this->needsInvite = true;
            return;
        }

        $invite = CustomerInvite::findUsable($token);
        if (! $invite) {
            $this->inviteInvalid = true;

            // Distinguish the failure mode for the user.
            $row = CustomerInvite::query()->where('token', $token)->first();
            if (! $row) {
                $this->inviteInvalidReason = 'unknown';
            } elseif ($row->isUsed()) {
                $this->inviteInvalidReason = 'used';
            } elseif ($row->isExpired()) {
                $this->inviteInvalidReason = 'expired';
            } else {
                $this->inviteInvalidReason = 'unknown';
            }
            return;
        }

        // Live verification: the customer must STILL be on the
        // monday.com Customer Details board. If they've been
        // archived (e.g. account closed), reject the token even
        // though the snapshot row still exists.
        try {
            $live = $directory->findByEmail($invite->email);
        } catch (\Throwable $e) {
            // If monday is unreachable, we still allow the
            // snapshot through — better to onboard a customer
            // than to lock them out because of an API outage.
            $live = null;
        }
        if ($live === null) {
            $this->inviteInvalid      = true;
            $this->inviteInvalidReason = 'monday_missing';
            return;
        }

        $this->email       = $invite->email;
        $this->accountName = $invite->account_name;
        $this->branch      = $invite->branch;
        $this->region      = $invite->region ?? '';
        $this->address     = $invite->address ?? '';
    }

    /**
     * Handle the registration. Customer-only. TSP/manager accounts
     * are provisioned from monday.com, never via this form.
     */
    public function register(): void
    {
        // Re-validate the token at submit time (not just at mount)
        // so a stale page doesn't bypass expiry.
        $invite = CustomerInvite::findUsable($this->token);
        if (! $invite) {
            $this->addError('token', 'This invite link is no longer valid. Please ask your service coordinator for a new one.');
            return;
        }

        $validated = $this->validate([
            'name'                => ['required', 'string', 'max:255'],
            'password'            => ['required', 'string', 'confirmed', Rules\Password::defaults()],
            // Snapshot fields are validated as "must match the
            // invite" so a tampered form can't slip a different
            // email through.
            'email'               => ['required', 'string', 'lowercase', 'email', 'max:191'],
            'accountName'         => ['required', 'string', 'max:255'],
            'branch'              => ['required', 'string', 'max:255'],
            'region'              => ['nullable', 'string', 'max:100'],
            'address'             => ['nullable', 'string', 'max:500'],
        ], [
            'email.required'    => 'Your email is missing. Please use the link your coordinator sent you.',
            'email.email'       => 'That email address looks invalid. Please contact your service coordinator.',
        ]);

        if (strtolower(trim($validated['email'])) !== strtolower(trim($invite->email))) {
            $this->addError('email', 'The email on this form does not match the invite. Please use the original link.');
            return;
        }
        if (trim($validated['accountName']) !== trim($invite->account_name)) {
            $this->addError('accountName', 'The account name on this form does not match the invite. Please use the original link.');
            return;
        }

        // Refuse to register an email that already has an account
        // — covers the case where two tokens were issued for the
        // same address.
        if (User::query()->where('email', $validated['email'])->exists()) {
            $this->addError('email', 'An account with this email already exists. Try signing in instead.');
            return;
        }

        $validated['password']         = Hash::make($validated['password']);
        $validated['role']             = 'customer';
        $validated['status']           = 'active';
        $validated['monday_id']        = $invite->monday_customer_id;

        // Map camelCase form property names to snake_case DB columns
        // for User::create(). The property is $accountName but the
        // users.account_name column is snake_case.
        $user = User::create([
            'name'         => $validated['name'],
            'email'        => $validated['email'],
            'password'     => $validated['password'],
            'role'         => $validated['role'],
            'status'       => $validated['status'],
            'monday_id'    => $validated['monday_id'],
            'branch'       => $validated['branch'],
            'region'       => $validated['region'] ?? null,
            'address'      => $validated['address'] ?? null,
            'account_name' => $validated['accountName'],
        ]);

        // Spend the token AFTER the user is created. A failed
        // create shouldn't lock out a legitimate customer.
        $invite->markUsed($user->id);

        event(new Registered($user));
        Auth::login($user);

        $this->redirect(route($user->homeRoute(), absolute: false), navigate: true);
    }

    /**
     * React to the password-strength JS hook from the input below.
     */
    public function updatedPasswordStrength(string $value): void
    {
        $this->passwordStrength = $value;
    }
}; ?>

<div>
